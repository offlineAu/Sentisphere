-- SCHEMA
CREATE TABLE user (
    user_id INT PRIMARY KEY AUTO_INCREMENT,
    email VARCHAR(100) UNIQUE DEFAULT NULL,
    name VARCHAR(100),
    role ENUM('student', 'counselor') NOT NULL,
    password_hash VARCHAR(255),
    nickname VARCHAR(50),
    last_login DATETIME,
    is_active BOOLEAN DEFAULT TRUE,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP
);
CREATE TABLE counselor_profile (
    user_id INT PRIMARY KEY,
    department VARCHAR(100),
    contact_number VARCHAR(20),
    availability VARCHAR(100),
    year_experience INT,
    phone VARCHAR(50),
    license_number VARCHAR(100),
    specializations TEXT,
    education TEXT,
    bio TEXT,
    languages VARCHAR(255),
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES user(user_id)
);
CREATE TABLE journal (
    journal_id INT PRIMARY KEY AUTO_INCREMENT,
    user_id INT,
    content TEXT,
    deleted_at DATETIME NULL,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES user(user_id)
);
CREATE TABLE journal_sentiment (
    journal_sentiment_id INT PRIMARY KEY AUTO_INCREMENT,
    journal_id INT,
    sentiment VARCHAR(50),
    emotions VARCHAR(255),
    confidence FLOAT,
    model_version VARCHAR(50),
    analyzed_at DATETIME,
    FOREIGN KEY (journal_id) REFERENCES journal(journal_id)
);
CREATE TABLE emotional_checkin (
    checkin_id INT PRIMARY KEY AUTO_INCREMENT,
    user_id INT,
    mood_level ENUM('Awesome', 'Great', 'Loved', 'Okay', 'Meh', 'Anxious', 'Bad', 'Terrible', 'Upset'),
    energy_level ENUM('Low', 'Moderate', 'High'),
    stress_level ENUM('No Stress', 'Low Stress', 'Moderate', 'High Stress', 'Very High Stress'),
    feel_better ENUM('Yes', 'No', 'Same'),
    comment TEXT,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES user(user_id)
);
CREATE TABLE checkin_sentiment (
    checkin_sentiment_id INT PRIMARY KEY AUTO_INCREMENT,
    checkin_id INT,
    sentiment VARCHAR(50),
    emotions VARCHAR(255),
    confidence FLOAT,
    model_version VARCHAR(50),
    analyzed_at DATETIME,
    FOREIGN KEY (checkin_id) REFERENCES emotional_checkin(checkin_id)
);
CREATE TABLE notification (
    notification_id INT PRIMARY KEY AUTO_INCREMENT,
    user_id INT,
    message TEXT,
    type VARCHAR(50),
    is_read BOOLEAN DEFAULT FALSE,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES user(user_id)
);
CREATE TABLE resource_log (
    resource_id INT PRIMARY KEY AUTO_INCREMENT,
    title VARCHAR(100),
    content TEXT,
    category VARCHAR(50),
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP
);
CREATE TABLE user_activities (
    activity_id INT PRIMARY KEY AUTO_INCREMENT,
    user_id INT,
    action VARCHAR(50),
    target_type VARCHAR(50),
    target_id INT,
    started_at DATETIME,
    ended_at DATETIME,
    duration_seconds INT,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES user(user_id)
);
CREATE TABLE alert (
    alert_id INT PRIMARY KEY AUTO_INCREMENT,
    user_id INT,
    reason VARCHAR(255),
    severity ENUM('low', 'medium', 'high', 'critical'),
    assigned_to INT,
    status ENUM('open', 'in_progress', 'resolved') DEFAULT 'open',
    resolved_at DATETIME,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES user(user_id),
    FOREIGN KEY (assigned_to) REFERENCES user(user_id)
);
CREATE TABLE conversations (
    conversation_id INT PRIMARY KEY AUTO_INCREMENT,
    initiator_user_id INT NOT NULL,
    initiator_role ENUM('student') NOT NULL,
    subject VARCHAR(100),
    counselor_id INT,
    status ENUM('open', 'ended') DEFAULT 'open',
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    last_activity_at DATETIME,
    FOREIGN KEY (initiator_user_id) REFERENCES user(user_id),
    FOREIGN KEY (counselor_id) REFERENCES user(user_id)
);
CREATE TABLE messages (
    message_id INT PRIMARY KEY AUTO_INCREMENT,
    conversation_id INT NOT NULL,
    sender_id INT NOT NULL,
    content TEXT NOT NULL,
    is_read BOOLEAN DEFAULT FALSE,
    timestamp DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (conversation_id) REFERENCES conversations(conversation_id),
    FOREIGN KEY (sender_id) REFERENCES user(user_id)
);
CREATE TABLE appointment_log (
    log_id INT PRIMARY KEY AUTO_INCREMENT,
    user_id INT,
    form_type VARCHAR(100),
    downloaded_at DATETIME,
    remarks TEXT,
    FOREIGN KEY (user_id) REFERENCES user(user_id)
);

CREATE TABLE ai_insights (
  insight_id INT PRIMARY KEY AUTO_INCREMENT,
  user_id INT DEFAULT NULL, -- NULL = platform-level insights
  type ENUM('weekly', 'behavioral') NOT NULL,
  timeframe_start DATE NOT NULL,
  timeframe_end DATE NOT NULL,
  data JSON NOT NULL, -- structured blob: {summary, mood_trends, top_concerns, metrics, metadata}
  risk_level ENUM('low','medium','high','critical') DEFAULT 'low',
  generated_by VARCHAR(100), -- e.g., "fastapi_v1"
  generated_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  UNIQUE KEY uniq_insight (user_id, type, timeframe_start, timeframe_end),
  FOREIGN KEY (user_id) REFERENCES user(user_id)
);


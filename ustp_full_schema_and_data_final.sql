-- ============================
-- NOTIFICATIONS
-- ============================
CREATE TABLE `notification` (
    `id` INT AUTO_INCREMENT PRIMARY KEY,
    `user_id` INT NOT NULL,
    `title` VARCHAR(150) NULL,
    `message` TEXT NOT NULL,
    `category` ENUM(
        'daily_quote',

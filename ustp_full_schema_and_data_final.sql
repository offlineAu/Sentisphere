-- ============================
-- USER ACTIVITIES
-- ============================
CREATE TABLE user_activities (
    activity_id INT PRIMARY KEY AUTO_INCREMENT,
    user_id INT,
    action VARCHAR(50),
    target_type VARCHAR(50),
    target_id INT,
    started_at DATETIME,
    ended_at DATETIME,
    duration_seconds INT,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
